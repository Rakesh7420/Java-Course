public class switchop {
    public static void main(String arg[]){
        int value=1;
        switch(value){
            case 1 : System.out.println("Burger");
                       break;
            case 2 : System.out.println("Samosa");
                       break;
            case 3 : System.out.println("Panipuri");
                       break;
            default:System.out.println("Any food");
        }
    }
}
