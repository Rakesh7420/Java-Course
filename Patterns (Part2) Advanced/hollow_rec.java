public class hollow_rec {
    public static void main(String[] args) {
        hollowr_ectangle(4, 5);
    }

    public static void hollowr_ectangle(int n, int m) {
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= m; j++) {
                if (i == 1 || i == n || j == 1 || j == m) {
                    System.out.print("*");
                } else {
                    System.out.print(" ");
                }
            }
            System.out.println();

        }
    }
}
